    private void jTextFieldNombreClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombreClienteKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==10)
        {
            if(jTextFieldNombreCliente.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "La caja de texto no puede estar vacía.", "ERROR.", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                if(jTextFieldNombreCliente.getText().length()>15)
                {
                    JOptionPane.showMessageDialog(this, "La caja de texto no admite más de 15 caracteres.", "ERROR.", JOptionPane.ERROR_MESSAGE);
                }
                else{
                    if(!jTextFieldNombreCliente.getText().matches("[a-zA-Z áéíóúÁÉÍÓÚ\\s]{1,15}")){
                        JOptionPane.showMessageDialog(this, "La caja de texto solo admite caracteres(no acepta guiones, ni puntos, ni números, etc).", "ERROR.", JOptionPane.ERROR_MESSAGE);
                        estadoInicial();
                    }else
                    {
                        estadoPostNombreClienteCorrecto();
                    }
                }
            }
        }
    }//GEN-LAST:event_jTextFieldNombreClienteKeyPressed

