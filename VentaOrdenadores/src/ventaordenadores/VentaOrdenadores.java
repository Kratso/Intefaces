    //Método que va mostrando el contenido de un fichero.
    public void mostrarVolcadoF() throws Exception{
        volcadoFichero();
        pintaVenta2(listaAuxiliar.get(0));
        if(listaAuxiliar.size()>1)
        {
           boolean salir=false;
           for(int i=1;i<listaAuxiliar.size();i++){
                int opcion=JOptionPane.showConfirmDialog(null, "Hay más ventas guardadas,¿quiere mostrar la siguiente? Si sale de la ventana será como si hubiera pulsado 'Cancelar'.","¿Continuar?",JOptionPane.OK_CANCEL_OPTION);//Devuelve un valor entero según la opción que escojamos de la ventana.
                switch(opcion)
                {
                    case JOptionPane.OK_OPTION://Si das a "aceptar" sigue con el bucle.

                            break;
                    case JOptionPane.CANCEL_OPTION://Si das a "cancelar" en la ventana anterior muestra este mensaje.
                            JOptionPane.showMessageDialog(this, "Mostrado de datos interrumpido.");//Mensaje informativo.
                            estadoInicial();
                            salir=true;
                            break;
                    case JOptionPane.CLOSED_OPTION://Si cierras la ventana anterior muestra este mensaje.
                            JOptionPane.showMessageDialog(this, "Mostrado de datos interrumpido.");//Mensaje informativo.
                            estadoInicial();
                            salir=true;
                            break;
                }
                if(salir){
                    break;
                }
                pintaVenta2(listaAuxiliar.get(i));
            }
           if(!salir){
               JOptionPane.showMessageDialog(this, "No hay más ventas guardadas. Fin de mostrado de datos.");//Mensaje informativo.
               estadoInicial();
           }
        }
        else{
            JOptionPane.showMessageDialog(this, "Solo hay una venta guardada. Fin de mostrado de ventas.");//Mensaje informativo.
            estadoInicial();
        }
        listaAuxiliar.clear();
    }
    
