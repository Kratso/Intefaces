public static void habilitarOperaciones(JButton a, JButton b, JButton c, JButton d, JButton e)//Método que habilita 5 JButton que se le pasen como parámetro.
{
    a.setEnabled(true);
    b.setEnabled(true);
    c.setEnabled(true);
    d.setEnabled(true);
    if(e!=null)
    e.setEnabled(true);
}

public static void deshabilitarOperaciones(JButton a, JButton b, JButton c, JButton d, JButton e)//Método que deshabilita 5 JButton que se le pasen como parámetro.
{
    a.setEnabled(false);
    b.setEnabled(false);
    c.setEnabled(false);
    d.setEnabled(false);
    e.setEnabled(false);
}

public static void deshabilitarOperaciones(JButton a)
{
    a.setEnabled(false);
}

public static void habilitarOperaciones(JButton a)
{
    a.setEnabled(true);
}

