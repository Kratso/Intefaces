    private void jButtonResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResultadoActionPerformed
        //Si se pulsa el botón "=".
        try {
            jLabelresultado.setText(pulsaIgual(operacion,estado));//Llama al método "pulsaIgual" en MetodosCalculadora.java.
            estado="terminado";
        /*
            Deshabilitamos todos los botones.
        */
        deshabilitarOperaciones(jButtonSuma, jButtonResta, jButtonMultiplicacion, jButtonDivision, jButtonResultado);
        deshabilitarOperaciones(jButton0, jButton1, jButton2, jButton3, jButton4);
        deshabilitarOperaciones(jButton5, jButton6, jButton7, jButton8, jButton9);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());//Si recibe una excepción muestra el mensaje inherente a ella.
        }
    }//GEN-LAST:event_jButtonResultadoActionPerformed

